package pacman.modele.lang;

public class PacmanException extends Exception {

	public PacmanException() {
		super();
	}

	public PacmanException(String arg0) {
		super(arg0);
	}

	public PacmanException(Throwable arg0) {
		super(arg0);
	}

	public PacmanException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
